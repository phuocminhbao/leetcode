const minimumTotal = (triangle) => {
    const memo = {};
    const getShortest = (row, index) => {
        if (memo[`${row}-${index}`] !== undefined) {
            return memo[`${row}-${index}`];
        }
        if (row === triangle.length - 1) {
            return triangle[row][index] ?? Infinity;
        }
        const left = getShortest(row + 1, index);
        const right = getShortest(row + 1, index + 1);
        const min = triangle[row][index] + Math.min(left, right);
        memo[`${row}-${index}`] = min;
        return min;
    };
    return getShortest(0, 0);
};

